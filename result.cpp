/******************************************************************************
* 関数名：UpdateResult
* 
* 引数  ：
* 戻り値：
* 説明  ：
******************************************************************************/
void UpdateResult( void )
{
	UpdateResultLogo();

	switch( g_ResultMode )
	{
	case RESULT_CREAL:
		{
			if( GetKeyboardTrigger( DIK_RETURN ) )
			{
				SetFade( FADE_OUT , MODE_SELECT );
			}
			break;
		}
	case RESULT_GAMEOVER:
		{
			if( GetKeyboardTrigger( DIK_RETURN ) )
			{
				SetFade( FADE_OUT , MODE_TITLE );
			}
			break;
		}
	}
}

